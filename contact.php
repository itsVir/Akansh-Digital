	<script>
    // Function to get query parameters
    function getQueryParam(param) {
        const urlParams = new URLSearchParams(window.location.search);
        return urlParams.get(param);
    }

    window.onload = function () {
        // Get the selected plan from the URL
        const selectedPlan = getQueryParam('plan');
        const contactTitleElement = document.querySelector('.contact-page__title');

        // If a plan is selected and the target element exists, scroll and pre-fill
        if (selectedPlan && contactTitleElement) {
            // Smooth scroll to the contact form title
            contactTitleElement.scrollIntoView({ behavior: 'smooth' });

            // Set subject text based on the selected plan
            let subjectText = '';
            switch (selectedPlan) {
                case 'basic':
                    subjectText = 'Want to buy Basic plan';
                    break;
                case 'standard':
                    subjectText = 'Want to buy Standard plan';
                    break;
                case 'premium':
                    subjectText = 'Want to buy Premium plan';
                    break;
                default:
                    subjectText = 'Interested in a plan';
            }

            // Fill the subject input if found
            const subjectInput = document.querySelector('input[name="subject"]');
            if (subjectInput) {
                subjectInput.value = subjectText;
            }
        }
    };
</script>






